#include <stdio.h>

/****************************************************************************
 *                                                                          *
 * Function: main                                                           *
 *                                                                          *
 * Print like this:
		g b
	   go ba
	  gol bal
	 golf ball		                                          *
 *                                                                          *
 *                                               *
 *                                             *
 *                                                                          *
 ****************************************************************************/

int main()

{	
	int i;
	for(i=0;i<4;i++)
	{
		printf("%4.*s  %-4.*s\n",i+1,"golf",i+1,"ball");
		}
		return 0;
}

